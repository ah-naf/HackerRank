int main()
{
    AHNAF;
    ll n,m,k;cin>>n>>m>>k; ll ans = n*m;
    map<ll, vp> v;
    for(int i=0; i<k; i++) {
        ll r,c1,c2;cin>>r>>c1>>c2;
        if(v.find(r) == v.end()) {
            vp tmp; tmp.pb(mp(c1,c2));
            v[r] = tmp;
        }
        else {
            vp tmp = v[r]; tmp.pb(mp(c1,c2));
            v[r] = tmp;
        }
    }
    for(auto it: v) {
        vp tmp = it.ss; SORT(tmp);
        //ll first = LLONG_MAX, last = 0;
        for(int i=0; i<tmp.size(); i++) {
            ll first = tmp[i].ff, last = tmp[i].ss;
            while(i+1 < tmp.size()) {
                if(tmp[i+1].ff <= last) last = max(last, tmp[i+1].ss);
                else break;
                i++;
            }
            ans -= (last-first+1);
            //cout<<first<<" "<<last<<endl;
        }
    }
    cout<<ans<<endl;

}
